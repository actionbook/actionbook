#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn navigate_debug_format() {
        let op = BackendOp::Navigate {
            target_id: "ABC123".into(),
            url: "https://example.com".into(),
        };
        let debug = format!("{op:?}");
        assert!(debug.contains("Navigate"));
        assert!(debug.contains("ABC123"));
    }

    #[test]
    fn clone_works() {
        let op = BackendOp::Evaluate {
            target_id: "T1".into(),
            expression: "1+1".into(),
            return_by_value: true,
        };
        let cloned = op.clone();
        match cloned {
            BackendOp::Evaluate {
                expression,
                return_by_value,
                ..
            } => {
                assert_eq!(expression, "1+1");
                assert!(return_by_value);
            }
            _ => panic!("wrong variant after clone"),
        }
    }

    #[test]
    fn dispatch_mouse_event_fields() {
        let op = BackendOp::DispatchMouseEvent {
            target_id: "T1".into(),
            event_type: "mousePressed".into(),
            x: 150.0,
            y: 300.0,
            button: "left".into(),
            click_count: 1,
        };
        match op {
            BackendOp::DispatchMouseEvent {
                x,
                y,
                click_count,
                ..
            } => {
                assert!((x - 150.0).abs() < f64::EPSILON);
                assert!((y - 300.0).abs() < f64::EPSILON);
                assert_eq!(click_count, 1);
            }
            _ => panic!("wrong variant"),
        }
    }

    #[test]
    fn create_target_with_window() {
        let op = BackendOp::CreateTarget {
            url: "about:blank".into(),
            window_id: Some(42),
            new_window: false,
        };
        match op {
            BackendOp::CreateTarget {
                url,
                window_id,
                new_window,
            } => {
                assert_eq!(url, "about:blank");
                assert_eq!(window_id, Some(42));
                assert!(!new_window);
            }
            _ => panic!("wrong variant"),
        }
    }
}
